package javaStudy1;

public class PrimitiveDataTypeExam {

	public static void main(String[] args) {
		//논리형 : 논리형은 크기가 1byte이고 true와 false 중에 한 가지 값을 가질 수 있다.
		boolean isFun = true;
		System.out.println(isFun);
		
		//문자형 : 문자는 작은따옴표를 이용하여 한 글자를 표현할 수 있다.
		char c = 'f';
		
		int x = 59;
		
		//값을 적을때는 뒤에 소문자 l이나 대문자 L을 적어야 합니다.
		long bing = 564618654L;
		
		//float에 값을 대입할 때는 실수 뒤에 소문자 f나 대문자 F를 붙여야 합니다.
		float f = 32.4f;
		
		double d = 6541315.3;
	}

}
